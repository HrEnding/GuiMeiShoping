package cn.com.guimei.pojo;


public class Superuser {

  private long suId;
  private String suUser;
  private String suPwd;


  public long getSuId() {
    return suId;
  }

  public void setSuId(long suId) {
    this.suId = suId;
  }


  public String getSuUser() {
    return suUser;
  }

  public void setSuUser(String suUser) {
    this.suUser = suUser;
  }


  public String getSuPwd() {
    return suPwd;
  }

  public void setSuPwd(String suPwd) {
    this.suPwd = suPwd;
  }

}
